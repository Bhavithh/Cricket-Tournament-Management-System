    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String usr = UserField1.getText();
        String pass =new String(PassField1.getPassword());
        try 
        {
          Class.forName("com.mysql.cj.jdbc.Driver");
        } 
        catch (Exception ex) 
        {
            ex.printStackTrace();
        }
        try
        {
            if(usr.isEmpty()||pass.isEmpty())
            {
                JOptionPane.showMessageDialog(btnNewButton, "Enter Username and Password!!");
                return;
            }
            Connection conn=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/cric_db","root","bk@1947");
            PreparedStatement st = (PreparedStatement) conn.prepareStatement("Select username, password from login where username=? and password=?");
            st.setString(1, usr);
            st.setString(2, pass);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                dispose();

                VHome obj=new VHome();
                obj.setVisible(true);
                setVisible(false);
                JOptionPane.showMessageDialog(btnNewButton,"You have successfully logged in");
            } 
            else 
            {
                JOptionPane.showMessageDialog(btnNewButton, "Invalid Username or Password:)");
                return;
            }

        }
        catch(SQLException sq)
        {
            sq.printStackTrace();
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

