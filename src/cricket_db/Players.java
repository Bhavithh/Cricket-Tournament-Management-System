    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        String plid=pid.getText();
        String plname=pname.getText();
        String team=tid.getText();
        int age=Integer.parseInt(page.getText());
        String prole=role.getSelectedItem().toString();
        String bats=bat.getSelectedItem().toString();
        String bowls=bowl.getSelectedItem().toString();
        try{
            if( plid.isEmpty() ||plname.isEmpty()||team.isEmpty()||prole.isEmpty()||bats.isEmpty()||bowls.isEmpty()){
                JOptionPane.showMessageDialog(btnNewButton,"Enter Value");
                return;
            }
            else if(age==0|| age>50 ||age<19)
            {
                JOptionPane.showMessageDialog(btnNewButton,"Enter a Valid age");
                return;
            }
            if(role.getSelectedItem()=="Batsman")
             {
                bowl.setSelectedItem("NA");
             }
            else if(role.getSelectedItem()=="Bowler")
            {
                bat.setSelectedItem("NA");
            }
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/cric_db","root", "bk@1947");
            PreparedStatement sm=(PreparedStatement) connection.prepareStatement("insert into players(pid,pname,age,role,team_id,bowl_sty,bat_sty) values(?,?,?,?,?,?,?)");
            sm.setString(1, plid);
            sm.setString(2,plname);
            sm.setInt(3,age );
            sm.setString(4,prole );
            sm.setString(5,team);
            sm.setString(6,bowls);
            sm.setString(7,bats);
            sm.executeUpdate();
            JOptionPane.showMessageDialog(btnNewButton,"Player added");
            display();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }

    }//GEN-LAST:event_SaveActionPerformed

    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        String plid = search.getText();
        try{
            if(plid.isEmpty())
            {
                JOptionPane.showMessageDialog(btnNewButton,"Enter value");
                return;
            }
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/cric_db","root","bk@1947");
            PreparedStatement tm1=(PreparedStatement) connection.prepareStatement("select pid,pname,age,role,team_id,bowl_sty,bat_sty from players where pid=?");
            tm1.setString(1, plid);
            ResultSet res=tm1.executeQuery();
            while(res.next()){
                pid.setText(res.getString("pid"));
                pid.setEditable(false);
                pname.setText(res.getString("pname"));
                int x=Integer.parseInt(res.getString("age"));
                page.setText(Integer.toString(x));
                role.setSelectedItem(res.getString("role"));
                tid.setText(res.getString("team_id"));
                bowl.setSelectedItem(res.getString("bowl_sty"));
                bat.setSelectedItem(res.getString("bat_sty"));
            }
        }
        catch(Exception sqlException)
        {
            sqlException.printStackTrace();
        }
    }//GEN-LAST:event_findActionPerformed

