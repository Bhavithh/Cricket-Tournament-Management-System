    public void display()
    {
        try{
            
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/cric_db","root", "bk@1947");
            PreparedStatement dst = (PreparedStatement) connection.prepareStatement("select a.pid as Player_ID,a.pname as Name,a.role as Role,a.age as Age,a.bowl_sty as Bowling,a.bat_sty Batting,b.tname as Team from teams b,players a where a.team_id=b.team_id;");
            ResultSet res=dst.executeQuery();
            player.setModel(DbUtils.resultSetToTableModel(res));
            player.setEnabled(false);   
        }
        catch(Exception sqlException){
            sqlException.printStackTrace();
        }
    }
